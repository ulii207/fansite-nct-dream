          <h1>NCT DREAM</h1>
        </div>
        <article>
          <p>
            <strong>NCT DREAM</strong> adalah sub-unit ketiga dari boy band
            asal Korea Selatan NCT, yang khusus beranggotakan remaja dengan
            usia belasan tahun. Pada awalnya, sub-unit ini memiliki sistem
            kelulusan di mana anggota yang melampaui usia 20 akan keluar, tapi
            pada 2020, sistem ini diganti dan menjadikan NCT Dream sebagai
            unit tetap NCT. Unit ini melakukan debutnya pada 25 Agustus 2016
            dengan lagu "Chewing Gum" dan tujuh orang anggota: Mark, Renjun,
            Jeno, Haechan, Jaemin, Chenle dan Jisung.
          </p>
        </article>
