          <h3>2021: Album Lengkap Pertama, Repackaged, dan NCT 2021, Hot Sauce & Hello Future</h3>
        </div>
        <br><br>
        <center>
        <img width="320" height="220" src="hotsauce.jpeg"></img>
        <img width="320" height="220" src="future.jpg"></img>
        </center>
        <br>
        <article>
          <div class="paragraph">
            <p>
              Setelah penantian cukup panjang sejak debut, NCT Dream akhirnya
              merilis album lengkap pertama mereka yang bertajuk Hot Sauce
              dengan tujuh anggota. Album ini memuat sepuluh lagu termasuk
              lagu utama yang juga berjudul "Hot Sauce". Pra-pesan untuk album
              ini mencapai 1,7 kopi, sukses memecahkan rekor mereka dari
              Reload dengan kenaikan 243%. Album ini menduduki peringkat
              tinggi pada Gaon Album Chart dan "Hot Sauce" menjadi lagu
              pertama dari sub-unit NCT yang debut pada Gaon Digital Chart di
              peringkat pertama. Kesuksesan ini menghasilkan nama "double
              million sellers" mengingat Hot Sauce terjual lebih dari 2 juta
              kopi pada 26 Mei. Promosi "Hot Sauce" berakhir setelah NCT
              Dream berhasil mendapatkan 8 kemenangan di acara musik seperti
              Music Bank, Show! Music Core, Inkigayo, Show Champion.
            </p>
          </div>
          <div class="p-1">
            <p>
              Hal ini disusul dengan album repackage yang bertajuk Hello
              Future dan memuat tiga lagu baru, termasuk lagu utama dengan
              judul "Hello Future". Penjualan Hot Sauce dan Hello Future
              mencapai lebih dari 3 juta kopi, menjadikan NCT Dream sebagai
              penyandang gelar "triple million seller". Senada dengan "Hot
              Sauce", "Hello Future" berhasil mendapatkan lebih dari 5
              kemenangan acara musik dari M Countdown, Music Bank, Show! Music
              Core, Inkigayo, Show Champion.
            </p>
          </div>
          <div>
            <p>
              Setelah diumumkan pada 13 November 2021 jika NCT akan kembali
              dengan proyek besar mereka, NCT Dream dikonfirmasi turut serta
              dalam album ketiga NCT bertajuk Universe. Unit mereka secara
              khusus merilis lagu berjudul "Dreaming" yang track video-nya
              dirilis pada 1 Desember 2021. Lagu ini menjadi satu-satunya
              lagu dari album Universe yang meraih peringkat pertama pada
              Billboard #HotTrendingSongs pada 20 Desember 2021.
            </p>
          </div>
        </article>
