          <h3>Pra-debut: SM Rookies dan NCT</h3>
        </div>
        <br><br>
        <center>
        <img width="500" height="325" src="pra.jpg"></img>
        </center>
        <br>
        <article>
          <p>
            Lima dari tujuh anggota grup ini dulunya merupakan bagian dari SM
            Rookies; Mark, Jeno dan Jisung diperkenalkan pada tahun 2013,
            Haechan pada tahun 2014 dan Jaemin pada tahun 2015. Mark
            sebelumnya telah melakukan debut dengan NCT U yang merupakan unit
            pertama NCT, dan bersama Haechan juga merupakan anggota dari
            NCT 127, unit kedua yang berbasis di Seoul.
          </p>
        </article>
        <br><br>
        <div class="sub">
          <h3>2016: Debut</h3>
        </div>
        <br><br>
        <center>
        <img width="500" height="325" src="debut.jpg"></img>
        </center>
        <br>
        <article>
          <p>
            NCT Dream diumumkan secara resmi sebagai unit ketiga dari NCT pada
            18 Agustus 2016. Jisung menjadi anggota pertama yang
            diperkenalkan, disusul oleh Chenle, Jeno, Haechan, Mark, Renjun
            dan terakhir Jaemin.Video klip untuk singel "Chewing Gum"
            dirilis pada 24 Agustus 2016 dengan dua versi, Korea dan Mandarin.
            Mereka melakukan debut pada 25 Agustus 2016 di M! Countdown,
            sedangkan singel dirilis secara digital pada 27 Agustus 2016.
          </p>
        </article>
        <br><br>
        <div class="sub">
          <h3>2017: The First dan We Young</h3>
        </div>
        <br><br>
        <center>
        <img width="500" height="325" src="weyoung.png"></img>
        </center>
        <br>
        <article>
          <div class="paragraph">
            <p>
              Pada 1 Februari 2017, NCT Dream mengumumkan akan merilis sebuah
              album singel pertama mereka yang berjudul "The First" pada 9
              Februari 2017. S.M. Entertainment mengumumkan bahwa Jaemin
              tidak dapat berpartisipasi dalam comeback kali ini karena
              masalah kesehatan. Unit ini melakukan comeback panggungnya di
              M Countdown, menampilkan lagu utama "My First and Last" dan lagu
              sisi-B "Dunk Shot". Pada 14 Februari, NCT Dream memperoleh
              kemenangan pertama mereka di program musik, yakni di episode
              ke-100 The Show. Ini menandakan kemenangan program musik pertama
              kalinya untuk unit NCT. NCT Dream dipilih sebagai duta resmi
              Piala Dunia U-20 FIFA Korea 2017. Pada 15 Februari, mereka
              merilis lagu tema yang berjudul "Trigger The Fever".
            </p>
          </div>
          <div>
            <p>
              Pada 17 Agustus , NCT Dream merilis single terbaru mereka yang
              berjudul " We Young" dan disertai dengan rilis nya 1st mini
              albumnya yang memiliki title yang sama seperti single utama
              mereka "We Young". Terdapat 6 lagu di dalam album NCT Dream "We
              Young". Di dalam album tersebut terdapat salah satunya lagu
              "Trigger The Fever" .
            </p>
          </div>
        </article>
        <br><br>
        <div class="sub">
          <h3>2018: NCT 2018: Empathy dan "We Go Up"</h3>
        </div>
        <br><br>
        <center>
        <img width="500" height="325" src="wegoup.jpg"></img>
        </center>
        <br>
        <article>
          <div class="paragraph">
            <p>
              NCT Dream berpromosi bersama NCT U dan 127 sebagai NCT 2018 pada
              paruh pertama tahun 2018. Mereka berpartisipasi dalam album
              studio pertama NCT, NCT 2018 Empathy, dengan singel berjudul
              "Go".
            </p>
          </div>
          <div>
            <p>
              Pada Agustus 2018, SM Entertainment mengumumkan bahwa NCT Dream
              akan merilis album mini keduanya, We Go Up, pada 3 September
              2018. Mark dikonfirmasi akan menjadi anggota pertama yang akan
              lulus dari grup ini sehubungan dengan usianya yang telah
              menginjak 20 tahun. Untuk memperingati hal itu, NCT Dream
              merilis lagu berjudul "Candle Light" sebagai bagian dari proyek
              SM Station.
            </p>
          </div>
        </article>
        <br><br>
        <div class="sub">
          <h3>2019: We Boom dan Tur</h3>
        </div>
        <br><br>
        <center>
        <img width="500" height="325" src="weboom.jpg"></img>
        </center>
        <br>
        <article>
          <div class="paragraph">
            <p>
              NCT Dream merilis "Don't Need Your Love" sebagai lagu kolaborasi
              dengan penyanyi HRVY. Lagu ini menjadi penanda beranjaknya para
              anggota ke fase yang lebih dewasa di mana mereka menunjukkan
              kebolehan bernyanyi, menari, dan rap. Lagu ini merupakan bagian
              dari SM Station Season 3. Pada bulan Juli, NCT Dream menjadi
              duta global pertama dari World Scout Foundation dan merilis lagu
              berbahasa Inggris berjudul "Fireflies" yang keuntungannya
              digunakan untuk membiayai berbagai kegiatan lembaga di
              negara-negara berpendapatan rendah. Menyusul pada 23 Juli, NCT
              Dream juga menampilkan lagu ini pada World Scout Jamboree ke 24
              di West Virginia, Amerika Serikat.
            </p>
          </div>
          <div class="p-1">
            <p>
              NCT Dream merilis album ketiga mereka bertajuk We Boom dengan
              lagu utama "Boom" pada 26 Juli. Album ini terjual lebih dari
              300.000 kopi dalam waktu satu bulan. Tidak hanya itu, We
              Boom juga menempati peringkat ketujuh pada tangga album
              Billboard. "Boom" meraih dua kemenangan pada acara musik The
              Show pada 6 Agustus dan 20 Agustus. Album ini juga
              mengantarkan NCT Dream untuk mendapatkan penghargaan Bonsang
              pada Golden Disc Awards yang ke-34 dan Seoul Music Awards tahun
              2020.NCT Dream kembali termasuk dalam jajaran daftar
              Billboard 21 di Bawah 21 untuk dua tahun berturut-turut dan
              meraih peringkat ke-13 pada bulan September. Selanjutnya, mereka
              juga merilis lagu bersama PrettyMuch yang berjudul "Up To
              You".
            </p>
          </div>
          <div>
            <p>
              Pada bulan November sampai Desember, NCT Dream menjalani konser
              internasional pertama mereka yang berjudul, The Dream Show.
            </p>
          </div>
        </article>
        <br><br>
        <div class="sub">
          <h3>
            2020: The Dream, Reload, dan Pengaturan Ulang Mekanisme Grup
          </h3>
        </div>
        <br><br>
        <center>
        <img width="500" height="325" src="thedream.jpeg"></img>
        </center>
        <br>
        <article>
          <div class="paragraph">
            <p>
              NCT Dream memperluas pangsa pasar mereka dengan merilis album
              kompilasi berjudul The Dream yang secara eksklusif berbahasa
              Jepang dan berisi lagu-lagu mereka yang sebelumnya sudah dirilis
              dalam bahasa Korea. Album ini meraih peringkat tinggi pada
              Oricon Albums Chart. Pada awal 2020, NCT Dream juga
              meneruskan tur internasional mereka, The Dream Show, di beberapa
              kota di Jepang dan juga negara-negara Asia Selatan. Sayangnya,
              mulai Februari, tur ini dihentikan karena pandemi COVID-19.
              Saat inilah isu tentang pengorganisasian ulang NCT Dream kembali
              merebak. Fans menuntut SM Entertainment memperlakukan NCT Dream
              selayaknya grup tetap dan menghentikan sistem kelulusan.
              Pada 14 April, SM Entertainment mengumumkan NCT Dream akan
              merilis album keempat mereka, Reload, dengan enam anggota yang
              tersisa, yakni Renjun, Jeno, Haechan, Jaemin, Chenle, dan
              Jisung.Di akhir masa promosi, SM Entertainment akhirnya
              mengumumkan konsep baru NCT Dream yang menghapuskan sistem
              kelulusan sehingga Mark, yang sebelumnya lulus setelah melampaui
              usia 20, akhirnya kembali sebagai anggota. NCT Dream
              kemudian menjadi grup tetap, seperti tuntutan fans, dan berperan
              sebagaimana unit lain NCT sehingga tiap-tiap anggotanya
              berpotensi mengambil bagian dalam unit-unit NCT lainnya di masa
              mendatang.
            </p>
          </div>
          <div class="p-1">
            <p>
              Menyambut pengumuman itu, pada 28 April, Reload berhasil meraih
              lebih dari 500.000 pra-pesan, memecahkan rekor untuk jumlah
              pra-pesan terbanyak NCT Dream selama ini.Pada 29 April, NCT
              Dream merilis album Reload dengan lagu utama "Ridin", lagu
              bernuansa urban trap yang menunjukkan aspirasi dan harapan NCT
              Dream dalam sistem terbarunya.Pada perilisannya, album
              Reload berhasil menduduki peringkat tinggi di tangga lagu iTunes
              pada 49 negara, termasuk Amerika Serikat,sekaligus
              menduduki peringkat tertinggi untuk penjualan album di situs
              musik terbesar China, QQ Music. Di Korea Selatan, "Ridin"
              meraih peringkat pertama di Melon, membuatnya sebagai lagu
              pertama yang dirilis NCT untuk menduduki peringkat tersebut.
              Kelima lagu dari album mini tersebut juga menduduki peringkat 10
              besar di tangga digital Melon.Pekan berikutnya, NCT Dream
              meraih peringkat pertama pada tangga Billboard Emerging
              Artists.Setelah peluncuran album keempat mereka, NCT Dream
              mengadakan konser daring bertajuk "NCT Dream - Beyond the Dream
              Show" sebagai bagian dari Beyond Live, yakni serangkaian konser
              langsung daring. Konser ini berlangsung pada 10 Mei.
            </p>
          </div>
          <div>
            <p>
              NCT Dream kemudian bergabung dengan unit lainnya di bawah
              naungan NCT dalam megaproyek NCT 2020, yang menandai kembalinya
              NCT dengan album keduanya, yakni NCT 2020 Resonance yang terdiri
              dari dua bagian dan dirilis berurutan pada Oktober dan November.
              Di akhir 2020, dua bagian Resonance terjual sebanyak lebih dari
              2,6 juta kopi yang menandai rekor baru penjualan NCT. Dalam
              album tersebut, termasuk di dalamnya lagu "Déjà Vu" yang
              ditampilkan oleh NCT Dream dan merupakan lagu pertama Mark sejak
              kembali ke NCT Dream sejak pengaturan ulangnya.
            </p>
          </div>
        </article>
